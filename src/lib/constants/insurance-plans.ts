// ─── Helper: Get all plans as a flat array ─────────────────────────────────────

export function getAllPlans(): InsurancePlan[] {
  return INSURER_PROFILES.flatMap((p) => p.plans);
}

export function getPlanById(id: string): InsurancePlan | undefined {
  return getAllPlans().find((p) => p.id === id);
}

export function getInsurerProfile(slug: string): InsurerProfile | undefined {
  return INSURER_PROFILES.find((p) => p.slug === slug);
}

export function getPlansByTier(tier: InsurancePlan["tier"]): InsurancePlan[] {
  return getAllPlans().filter((p) => p.tier === tier);
}

export function getTierLabel(tier: InsurancePlan["tier"]): string {
  const labels: Record<InsurancePlan["tier"], string> = {
    basic: "Basic",
    enhanced: "Enhanced",
    premium: "Premium",
    vip: "VIP",
  };
  return labels[tier];
}

export function formatPremium(range: InsurancePlan["premiumRange"]): string {
  if (range.min === 0 && range.max === 0) return "Government-funded";
  if (range.min === range.max) return `AED ${range.min.toLocaleString()}/yr`;
  return `AED ${range.min.toLocaleString()} – ${range.max.toLocaleString()}/yr`;
}

export function formatLimit(amount: number): string {
  if (amount === 0) return "Unlimited";
  if (amount >= 1000000) return `AED ${(amount / 1000000).toFixed(1)}M`;
  return `AED ${amount.toLocaleString()}`;
}
