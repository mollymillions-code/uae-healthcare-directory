/**
 * Detailed insurance plan data for the UAE Health Insurance Navigator.
 *
 * Pricing and coverage details are indicative ranges based on publicly available
 * UAE insurance market data (2024-2025). Actual premiums vary by age, nationality,
 * visa type, and employer. Users are advised to get quotes directly from insurers.
 */

export interface InsurancePlan {
  id: string;
  insurerSlug: string;
  name: string;
  tier: "basic" | "enhanced" | "premium" | "vip";
  /** Annual premium range in AED */
  premiumRange: { min: number; max: number };
  /** Target audience */
  targetAudience: string;
  /** Coverage highlights */
  coverage: {
    inpatient: boolean;
    outpatient: boolean;
    dental: boolean;
    optical: boolean;
    maternity: boolean;
    mentalHealth: boolean;
    preventive: boolean;
    homeHealthcare: boolean;
    alternativeMedicine: boolean;
    internationalCoverage: boolean;
  };
  /** Annual coverage limit in AED (0 = unlimited) */
  annualLimit: number;
  /** Room type for inpatient */
  roomType: "shared" | "semi-private" | "private" | "suite";
  /** Co-pay percentage for outpatient (0 = no co-pay) */
  copayOutpatient: number;
  /** Co-pay percentage for pharmacy */
  copayPharmacy: number;
  /** Pre-existing condition waiting period in months */
  preExistingWaitMonths: number;
  /** Maternity waiting period in months (0 = covered from day 1, -1 = not covered) */
  maternityWaitMonths: number;
  /** Dental annual sub-limit in AED (0 = not covered) */
  dentalLimit: number;
  /** Optical annual sub-limit in AED (0 = not covered) */
  opticalLimit: number;
  /** Key exclusions */
  exclusions: string[];
  /** Key benefits / differentiators */
  highlights: string[];
  /** DHA/HAAD/MOHAP compliant */
  regulatoryCompliance: ("dha" | "haad" | "mohap")[];
  /** Network size label */
  networkTier: "basic" | "standard" | "enhanced" | "premium";
}

export interface InsurerProfile {
  slug: string;
  name: string;
  type: "mandatory" | "private" | "premium" | "tpa";
  foundedYear: number;
  headquarters: string;
  /** Regulator the insurer primarily operates under */
  regulators: ("dha" | "haad" | "mohap")[];
  website: string;
  claimsPhone: string;
  /** Key facts about the insurer */
  keyFacts: string[];
  /** Plans offered */
  plans: InsurancePlan[];
}

