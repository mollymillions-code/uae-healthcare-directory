// ─── Types ──────────────────────────────────────────────────────────────────────

export interface CityPricing {
  min: number;
  max: number;
  typical: number;
}

export interface MedicalProcedure {
  slug: string;
  name: string;
  nameAr: string;
  /** Maps to CATEGORIES in categories.ts */
  categorySlug: string;
  /** CPT code for cross-referencing with official tariffs */
  cptCode: string;
  description: string;
  /** What happens during this procedure (patient-facing explanation) */
  whatToExpect: string;
  /** DOH Mandatory Tariff base price in AED (where applicable) */
  baseTariffAed: number;
  /** Observed price range across all UAE facilities */
  priceRange: { min: number; max: number };
  /** City-specific pricing — keyed by city slug */
  cityPricing: Record<string, CityPricing>;
  /** How commonly covered by insurance */
  insuranceCoverage: "typically-covered" | "partially-covered" | "rarely-covered" | "not-covered";
  /** Insurance coverage notes for the patient */
  insuranceNotes: string;
  /** Average procedure duration */
  duration: string;
  /** Recovery time */
  recoveryTime: string;
  /** Whether this is inpatient, outpatient, or either */
  setting: "inpatient" | "outpatient" | "day-case" | "either";
  /** Related procedure slugs */
  relatedProcedures: string[];
  /** Common search terms / aliases for SEO */
  searchTerms: string[];
  /** Whether anaesthesia is typically required */
  anaesthesia: "none" | "local" | "sedation" | "general" | "varies";
  sortOrder: number;
}

export interface ProcedureCategory {
  slug: string;
  name: string;
  description: string;
  icon: string;
  procedureCount?: number;
}

