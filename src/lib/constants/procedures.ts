export function getProcedureBySlug(slug: string): MedicalProcedure | undefined {
  return PROCEDURES.find((p) => p.slug === slug);
}

export function getProceduresByCategory(procCategory: string): MedicalProcedure[] {
  // Map procedure category slugs to directory category slugs
  const categoryMap: Record<string, string[]> = {
    diagnostics: ["radiology-imaging", "labs-diagnostics"],
    dental: ["dental"],
    "eye-care": ["ophthalmology"],
    surgical: ["hospitals", "gastroenterology"],
    orthopedic: ["orthopedics"],
    maternity: ["ob-gyn", "fertility-ivf"],
    cosmetic: ["cosmetic-plastic", "dermatology"],
    cardiac: ["cardiology"],
    wellness: ["clinics"],
    therapy: ["physiotherapy", "mental-health"],
  };

  const directorySlugs = categoryMap[procCategory] || [];
  return PROCEDURES.filter((p) => directorySlugs.includes(p.categorySlug)).sort(
    (a, b) => a.sortOrder - b.sortOrder
  );
}

export function getAllProcedureSlugs(): string[] {
  return PROCEDURES.map((p) => p.slug);
}

export function searchProcedures(query: string): MedicalProcedure[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];

  return PROCEDURES.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.nameAr.includes(q) ||
      p.searchTerms.some((t) => t.toLowerCase().includes(q)) ||
      p.description.toLowerCase().includes(q) ||
      p.cptCode.includes(q)
  ).sort((a, b) => {
    // Exact name match first
    const aExact = a.name.toLowerCase().startsWith(q) ? 0 : 1;
    const bExact = b.name.toLowerCase().startsWith(q) ? 0 : 1;
    if (aExact !== bExact) return aExact - bExact;
    return a.sortOrder - b.sortOrder;
  });
}

export function formatAed(amount: number): string {
  return `AED ${amount.toLocaleString()}`;
}

export function getProcedureCategoryBySlug(slug: string): ProcedureCategory | undefined {
  return PROCEDURE_CATEGORIES.find((c) => c.slug === slug);
}
