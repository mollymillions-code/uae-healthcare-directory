/* ─── البيانات الوصفية ─── */

export function generateMetadata({ params }: { params: { slug: string } }): Metadata {
  const guide = GUIDES.find((g) => g.slug === params.slug);
  if (!guide) return {};
  const base = getBaseUrl();

  return {
    title: `${guide.title}`,
    description: guide.metaDescription,
    alternates: {
      canonical: `${base}/ar/insurance/guide/${guide.slug}`,
      languages: {
        "en-AE": `${base}/insurance/guide/${guide.slug}`,
        "ar-AE": `${base}/ar/insurance/guide/${guide.slug}`,
      },
    },
    openGraph: {
      title: guide.title,
      description: guide.metaDescription,
      url: `${base}/ar/insurance/guide/${guide.slug}`,
      type: "article",
    },
  };
}

