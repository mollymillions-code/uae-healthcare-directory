export async function generateMetadata({
  params,
}: {
  params: { slug: string };
}): Promise<Metadata> {
  const guide = GUIDE_ARTICLES.find((g) => g.slug === params.slug);
  if (!guide) return {};

  const base = getBaseUrl();
  return {
    title: `${guide.title} | دليل الإمارات المفتوح للرعاية الصحية`,
    description: guide.metaDescription,
    alternates: {
      canonical: `${base}/ar/directory/guide/${guide.slug}`,
      languages: {
        "en-AE": `${base}/directory/guide/${guide.slug}`,
        "ar-AE": `${base}/ar/directory/guide/${guide.slug}`,
        "x-default": `${base}/directory/guide/${guide.slug}`,
      },
    },
  };
}

